    public static String createSpan(CharSequence fullIconClassName) {
        return createSpan(fullIconClassName, null);
    }

    public static String createSpan(CharSequence fullIconClassName, CharSequence optional_text) {
        return createSpan(fullIconClassName, optional_text, false, false);
    }

    public static String createSpan(CharSequence fullIconClassName, CharSequence optional_text, boolean spaceFront, boolean spaceAfter) {
        var empty = "";
        var spc = TGS_NetworkHTMLUtils.HTML_SPACE();
        if (optional_text == null || optional_text.toString().isEmpty()) {
            return TGS_StringUtils.cmn().concat((spaceFront ? spc : empty), "<span class=\"", fullIconClassName, "\"></span>", (spaceAfter ? spc : empty));
        }
        return TGS_StringUtils.cmn().concat((spaceFront ? spc : empty), "<span class=\"", fullIconClassName, "\"></span>", spc, optional_text, (spaceAfter ? spc : empty));
    }
